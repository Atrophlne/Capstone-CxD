public class Encap {
   private String Program;
   private String Description;
   private String Acro;

   public String getAcro() {
      return Acro;
   }

   public void setAcro(String AcroN) {
      this.Acro = AcroN;
   }

   public String getProgram() {
      return Program;
   }

   public void setProgram(String Pram) {
      this.Program = Pram;
   }

   public String getDescrip() {
      return Description;
   }

   public void setDescrip(String Desc) {
      this.Description = Desc;
   }
}
